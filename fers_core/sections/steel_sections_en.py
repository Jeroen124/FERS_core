def resolve_section(name: str, material: Material) -> Section:
    """
    Look up a standard section by designation and return a fully-constructed
    :class:`Section`.

    Parameters
    ----------
    name : str
        Profile designation, e.g. ``"IPE200"``, ``"HEB300"``,
        ``"RHS 200x100x8"``, ``"SHS 100x100x6"``,
        ``"L 100x100x10"``, ``"CHS 168.3x5"``, ``"UPE200"``.
    material : Material
        The material object to assign to the section.

    Raises
    ------
    ValueError
        If the name is not found in the built-in library.
    """
    key = name.strip().upper()
    idx = _get_index()

    if key not in idx:
        # Try stripping extra spaces for hollow sections  "RHS  200x100x8"
        key = " ".join(key.split())

    if key not in idx:
        available = ", ".join(sorted(idx.keys())[:20]) + " …"
        raise ValueError(f"Section '{name}' not found in library. " f"Available (first 20): {available}")

    family, params = idx[key]

    if family in ("IPE",):
        return Section.create_ipe_section(
            name=name,
            material=material,
            h=params["h"],
            b=params["b"],
            t_w=params["t_w"],
            t_f=params["t_f"],
            r=params["r"],
        )

    if family in ("HEA", "HEB", "HEM"):
        return Section.create_he(
            name=name,
            material=material,
            h=params["h"],
            b=params["b"],
            t_w=params["t_w"],
            t_f=params["t_f"],
            r=params["r"],
        )

    if family == "UPE":
        return Section.create_u_section(
            name=name,
            material=material,
            h=params["h"],
            b=params["b"],
            t_w=params["t_w"],
            t_f=params["t_f"],
            r=params["r"],
        )

    if family == "CHS":
        return Section.create_chs(
            name=name,
            material=material,
            diameter=params["d"],
            thickness=params["t"],
        )

    if family == "RHS":
        return Section.create_rhs(
            name=name,
            material=material,
            h=params["h"],
            b=params["b"],
            t=params["t"],
        )

    if family == "SHS":
        return Section.create_shs(
            name=name,
            material=material,
            b=params["b"],
            t=params["t"],
        )

    if family == "L":
        return Section.create_angle_section(
            name=name,
            material=material,
            h=params["h"],
            b=params["b"],
            t=params["t"],
            r_root=params.get("r_root", 0.0),
            r_toe=params.get("r_toe", 0.0),
        )

    raise ValueError(f"Unknown family '{family}' for section '{name}'.")


def list_sections(series: Optional[str] = None) -> list[str]:
    """
    Return all available section names, optionally filtered by series.

    Parameters
    ----------
    series : str, optional
        One of ``"IPE"``, ``"HEA"``, ``"HEB"``, ``"HEM"``, ``"UPE"``,
        ``"CHS"``, ``"RHS"``, ``"SHS"``, ``"L"``.  If *None*, all names
        are returned.
    """
    tables: dict[str, dict] = {
        "IPE": _IPE,
        "HEA": _HEA,
        "HEB": _HEB,
        "HEM": _HEM,
        "UPE": _UPE,
        "CHS": _CHS,
        "RHS": _RHS,
        "SHS": _SHS,
        "L": _L,
    }

    if series is not None:
        key = series.strip().upper()
        if key not in tables:
            raise ValueError(f"Unknown series '{series}'. Choose from: {', '.join(tables)}")
        return sorted(tables[key].keys())

    result: list[str] = []
    for tbl in tables.values():
        result.extend(tbl.keys())
    return sorted(result)
